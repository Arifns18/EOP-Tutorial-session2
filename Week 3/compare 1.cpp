#include <iostream>

using namespace std;

int main(){
	
	int month;
	
	cout << "Enter month as integer (0-12): ";
	cin >> month;
	
	if (month == 1)
	cout << "January" << endl;
	else if (month == 2)
	cout << "February" << endl;
	else if (month == 3)
	cout << "March" << endl;
	else if (month == 4)
	cout << "April" << endl;
	else if (month == 5)
	cout << "May" << endl;
	else if (month == 6)
	cout << "June" << endl;
	
	return 0;
}


